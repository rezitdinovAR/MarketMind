def merge_data(file_path, new_categories):
    with open(file_path, "r", encoding="utf-8") as f:
        data = json.load(f)

    total_added = 0
    for category, products in new_categories.items():
        if category in data["categories"]:
            data["categories"][category].extend(products)
        else:
            data["categories"][category] = products
        total_added += len(products)

    with open(file_path, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    total = sum(len(prods) for prods in data["categories"].values())
    cats = len(data["categories"])
    print(f"  {file_path.name}: +{total_added} products, total={total}, categories={cats}")
    return total


